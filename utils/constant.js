export const getCurrentURL = (tenant = false, getTenant = false) => {
    if (typeof window !== "undefined") {
        let url = window.location.href;
        if (tenant) {
            let link = url.split(".");
            if (getTenant) {
                const tenantSolo = link[0].split("://");
                return tenantSolo[1];
            } else {
                return link[0];
            }
        } else {
            return url;
        }
    }
};

export const getHost = () => {
    if (typeof window !== "undefined") {
        let host = window.location.host.split(".");
        if (host.length > 1 && !host[0].includes('localhost')) {
            return host[1];
        } else {
            return 'demo'
        }
    } else {
        return "";
    }
};

export const redirectTo = (url, newWindow = false) => {
    const link = document.createElement("a");
    link.href = url;
    link.target = newWindow ? "_blank" : "_self";
    link.click();
};

